package collectionframewrok;

import java.util.HashMap;
import java.util.Map;

public class Hashmaps {

	public static void main(String[] args) {

		HashMap<Integer, String> hmap = new HashMap<>();  //Key Value pairs for Hashmaps
		
		hmap.put(101, "John");
		hmap.put(102, "Mary");
		hmap.put(103, "Ron");
		
		for (Integer i : hmap.keySet()) {
		
		System.out.println("Roll ID and Name is: " +hmap.get(i));
		
		
			}
		
		System.out.println("---------------------------------------------------");
		
		
		for(Map.Entry<Integer, String> map : hmap.entrySet()) {
			System.out.println("Roll ID is: " +map.getKey()+ " and Name is: " +map.getValue());
		}
	}

}
